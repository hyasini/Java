public class Task {
    String name;
    String gender;
    int age;
    int year;
    String course;
    String University;
	
    public Task(String name, String gender, int age, int year, String course, String university) {
		this.name = name;
		this.gender = gender;
		this.age = age;
		this.year = year;
		this.course = course;
		this.University = university;
	}
  
}